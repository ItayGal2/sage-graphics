// mmap.h++

#ifndef _MMAP
#define _MMAP

#include <unistd.h>
#include <sys/mman.h>
#include <sys/types.h>
#include <fcntl.h>
#include <sys/stat.h>
#include <string.h>

class Mmap {
    public:
        Mmap();
        ~Mmap();

        int setFilename(const char *filename, int create = 0);
        void setSize(size_t size);
        void setShared(int shared) { _shared = shared; }
        void setProt(int prot) { _prot = prot; }
        void bzero();

        caddr_t doMmap();

        ssize_t getSize() { return _len; }
        const char *getFilename() { return _filename; }

    private:
        char *_filename;
        size_t _len;
        size_t _fileSize;
        int _shared, _prot;
        int _fd;
        caddr_t _mmapAddress;

};

#endif
